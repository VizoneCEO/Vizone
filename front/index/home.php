<script>
    document.addEventListener('DOMContentLoaded', function () {
        const tabs = document.querySelectorAll('.showcase-tab-btn');
        const contents = document.querySelectorAll('.showcase-content');

        tabs.forEach(tab => {
            tab.addEventListener('click', function () {
                // Si ya está activo, no hacer nada
                if (this.classList.contains('active')) return;

                // Remover active de todos los tabs
                tabs.forEach(t => {
                    t.classList.remove('active');
                    if (t.querySelector('.tab-indicator')) t.querySelector('.tab-indicator').classList.replace('bg-active', 'bg-inactive');
                    if (t.querySelector('.tab-title')) {
                        t.querySelector('.tab-title').classList.remove('text-white');
                        t.querySelector('.tab-title').classList.add('text-white', 'opacity-50');
                    }
                    if (t.querySelector('.tab-desc')) t.querySelector('.tab-desc').classList.replace('d-block', 'd-none');
                });

                // Añadir active al clickeado
                this.classList.add('active');
                if (this.querySelector('.tab-indicator')) this.querySelector('.tab-indicator').classList.replace('bg-inactive', 'bg-active');
                if (this.querySelector('.tab-title')) {
                    this.querySelector('.tab-title').classList.remove('opacity-50');
                    this.querySelector('.tab-title').classList.add('text-white');
                }
                if (this.querySelector('.tab-desc')) this.querySelector('.tab-desc').classList.replace('d-none', 'd-block');

                // Ocultar todos los contenidos y mostrar el correspondiente
                const targetId = this.getAttribute('data-target');
                if (contents && contents.length > 0) {
                    contents.forEach(c => {
                        if (c.id === targetId) {
                            c.classList.remove('opacity-0', 'pointer-events-none', 'scale-95', 'position-absolute', 'top-0', 'start-0');
                            c.classList.add('opacity-100', 'pointer-events-auto', 'scale-100', 'active-content', 'position-relative'); // active-content dispara animaciones
                            c.style.zIndex = '2';
                        } else {
                            c.classList.remove('opacity-100', 'pointer-events-auto', 'scale-100', 'active-content', 'position-relative');
                            c.classList.add('opacity-0', 'pointer-events-none', 'scale-95', 'position-absolute', 'top-0', 'start-0');
                            c.style.zIndex = '1';
                        }
                    });
                }
            });
        });
    });
    // Lógica para Formulario de Auditoría
    const auditForm = document.getElementById('auditForm');
    if (auditForm) {
        auditForm.addEventListener('submit', async function (e) {
            e.preventDefault();

            if (!this.checkValidity()) {
                this.classList.add('was-validated');
                return;
            }

            const alertBox = document.getElementById('auditAlert');
            const btnSubmit = document.getElementById('btnSubmitAudit');

            // UI Loading state
            btnSubmit.disabled = true;
            btnSubmit.innerHTML = '<span class="spinner-border spinner-border-sm me-2" role="status" aria-hidden="true"></span> Procesando Reserva...';
            alertBox.classList.add('d-none');
            alertBox.classList.remove('alert-danger', 'alert-success');

            const formData = new FormData(this);

            try {
                const response = await fetch('/vizone/back/api/agendar_auditoria.php', {
                    method: 'POST',
                    body: formData
                });

                const data = await response.json();

                if (data.status === 'success') {
                    // Mostrar estado de éxito y ocultar formulario
                    auditForm.classList.add('d-none');
                    const successState = document.getElementById('audit-success-state');
                    successState.classList.remove('d-none');
                    successState.classList.add('d-block');

                    // Generar QR usando API pública (Añadir al Calendario)
                    const calendarUrl = data.calendar_url;
                    const qrContainer = document.getElementById('qr-container');
                    qrContainer.innerHTML = `<img src="https://api.qrserver.com/v1/create-qr-code/?size=200x200&data=${encodeURIComponent(calendarUrl)}&color=030303" alt="QR Code Add to Calendar" class="img-fluid rounded">`;

                    // Obtener número de teléfono y generar texto para WhatsApp
                    let rawPhone = document.getElementById('auditPhone').value.replace(/[^0-9]/g, '');
                    const aDate = document.getElementById('auditDate').value;
                    const aTime = document.getElementById('auditTime').options[document.getElementById('auditTime').selectedIndex].text;
                    const whatsappMsg = `¡Hola! Mi Auditoría con Vizone está confirmada para el ${aDate} a las ${aTime}.\n\nPara agendar en mi calendario:\n${calendarUrl}`;

                    document.getElementById('btn-meet-link').href = `https://api.whatsapp.com/send?phone=${rawPhone}&text=${encodeURIComponent(whatsappMsg)}`;

                } else {
                    // Mostrar error (Ej. Horario Duplicado)
                    alertBox.textContent = data.message || 'Error al procesar la solicitud.';
                    alertBox.classList.add('alert-danger', 'd-block');
                    alertBox.classList.remove('d-none');

                    // Reset UI
                    btnSubmit.disabled = false;
                    btnSubmit.innerHTML = `<span class="position-relative z-1 d-flex justify-content-center align-items-center gap-2">
                                        <i class="bi bi-calendar-plus fs-5"></i>
                                        Cerrar Mi Espacio Ahora
                                    </span>`;
                }

            } catch (error) {
                alertBox.textContent = 'Ocurrió un error inesperado al conectar con el servidor.';
                alertBox.classList.add('alert-danger', 'd-block');
                alertBox.classList.remove('d-none');

                btnSubmit.disabled = false;
                btnSubmit.innerHTML = `<span class="position-relative z-1 d-flex justify-content-center align-items-center gap-2">
                                        <i class="bi bi-calendar-plus fs-5"></i>
                                        Cerrar Mi Espacio Ahora
                                    </span>`;
            }
        });
    }

</script>